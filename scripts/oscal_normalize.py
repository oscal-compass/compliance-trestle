def apply_eligible(line):
    """Apply eligible for token replacement.

    We want to replace class names in type annotations but not in string literals.
    For __root__ lines with Field(...), we need to replace class names in the type
    annotation (before = Field) but not in the description/title strings.
    """
    # If no title= or description=, always eligible
    if 'title=' not in line and 'description=' not in line:
        return True

    # Special case for CommonRiskStatus
    if 'CommonRiskStatus' in line:
        return True

    # For __root__ lines with type annotations, we want to replace class names
    # in the type annotation part only (before ' = Field(')
    if '__root__:' in line and ' = Field(' in line:
        return True

    # Otherwise, skip lines with title=/description= to avoid changing strings
    return False


def apply_changes_to_class_list(classes, changes):
    """Make all changes to the name and body of a list of classes."""
    for i, c in enumerate(classes):
        lines = []
        for line in c.lines:
            if apply_eligible(line):
                for item in changes:
                    if item[0] in line:
                        line = replace_token(line, item[0], item[1])
            lines.append(line)
        classes[i].lines = lines

        # make sure class definition has correct name
        paren = lines[0].find('(')
        class_name = classes[i].name
        if paren > 0:
            class_name = lines[0][len('class ') : paren]
        classes[i].name = class_name
        # need to regenerate body since tokens changed
        classes[i].generate_body_text()
    return classes


def apply_changes_to_classes(file_classes, changes, com_names):
    """Apply changes to dict of classes organized by file."""
    for fc in file_classes.items():
        classes = fc[1]
        for i, c in enumerate(classes):
            lines = []
            for line in c.lines:
                if 'title=' not in line and 'description=' not in line:
                    for item in changes.items():
                        if item[0] not in line:
                            continue
                        new_name = item[1]
                        # if not in common then need to add common. to common names
                        if fc[0] != 'common' and new_name in com_names:
                            tentative_name = 'common.' + new_name
                            if tentative_name not in line:
                                new_name = tentative_name
                        line = replace_token(line, item[0], new_name)
                lines.append(line)
            classes[i].lines = lines

            # class name may have been replaced by change - so update with new name
            paren = lines[0].find('(')
            class_name = classes[i].name
            if paren > 0:
                class_name = lines[0][len('class ') : paren]
            classes[i].name = class_name
            classes[i].generate_body_text()
        file_classes[fc[0]] = classes
    return file_classes


