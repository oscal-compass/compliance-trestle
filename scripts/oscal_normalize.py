def refine_split(file_classes):
    """Make sure no references in common link to the other files."""
    # get list of original names in current common file
    common_names = []
    for c in file_classes['common']:
        common_names.append(c.unique_name)

    # find all original names of classes in other files that shouldn't be refd by common
    names = set()
    for stem in fstems:
        for c in file_classes[stem]:
            if (c.is_local) or (c.unique_name not in common_names):
                names.add(c.unique_name)
    names = list(names)

    # if any common class references outside common - exclude it from common
    not_com = []
    for c in file_classes['common']:
        # Force Group1 out of common - it references Group2 which is file-specific
        if c.name == 'Group1':
            not_com.append(c.name)
            continue
        excluded = False
        for line in c.lines:
            if excluded:
                break
            if '"' not in line and "'" not in line:
                for name in names:
                    if token_in_line(line, name):
                        not_com.append(c.name)
                        excluded = True
                        break

    # remove all not_com from com and add to other files as needed by parents
    new_com = []
    for c in file_classes['common']:
        if c.name in not_com:
            for parent in c.parent_names:
                file_classes[parent].append(c)
        else:
            new_com.append(c)
    file_classes['common'] = new_com
    return file_classes


def _find_in_classes(name, file_classes):
    # debugging utility
    found = []
    for item in file_classes.items():
        for c in item[1]:
            if name in c.name:
                found.append((item[0], c.name))
    return found


def _find_in_class_list(name, classes):
    # debugging utility
    found = []
    for c in classes:
        if name in c.name:
            found.append((name, c.name))
    return found


def split_classes(classes):
    """Split into separate common and other files."""
    file_classes = {}
    for stem in fstems:
        file_classes[stem] = []
    file_classes['common'] = []
    com_names = []

    for c in classes:
        if is_common(c):
            if c.name not in com_names:
                com_names.append(c.name)
                file_classes['common'].append(c)
        else:
            # remove clash prefix from the class name if present
            # the prefix is removed from bodies after the split
            c.name = c.name.split('_')[-1]
            for parent in c.parent_names:
                # the class carries with it that it is local and bound to the parent
                file_classes[parent].append(c)

    # keep removing classes in com that have external dependencies until it is clean
    new_ncom = 0
    while new_ncom != len(file_classes['common']):
        new_ncom = len(file_classes['common'])
        file_classes = refine_split(file_classes)
    return file_classes


def reorder_classes(fstem, classes):
    """Reorder the classes to minimize needed forwards."""
    classes = sorted(classes, key=lambda c: c.name)
    new_classes = []
    for c in classes:
        for line in c.lines:
            _ = c.add_all_refs(line)
        new_classes.append(c)
    reordered, forward_refs = reorder(fstem, new_classes)
    return reordered, forward_refs


def write_oscal(classes, forward_refs, fstem):
    """Write out oscal.py with all classes in it."""
    with open(f'trestle/oscal/{fstem}.py', 'w', encoding='utf8') as out_file:
        is_common = fstem == 'common'

        out_file.write(license_header)
        out_file.write('\n')
        out_file.write(main_header)

        if not is_common:
            out_file.write('import trestle.oscal.common as common\n')
        out_file.write('\n\n')

        # Track if we've seen both Parameter1 and Parameter2 for adding the alias
        seen_parameter1 = False
        seen_parameter2 = False
        # Track if we've seen both Parties and Parties1 for adding the alias
        seen_parties = False
        seen_parties1 = False
        # Track profile.py Union variants for creating aliases
        seen_merge1 = False
        seen_merge2 = False
        seen_merge3 = False
        seen_import1 = False
        seen_import2 = False
        seen_insertcontrols1 = False
        seen_insertcontrols2 = False
        seen_setparameters = False

        for c in classes:
            # Skip the broken Group Union wrapper in common.py that references Cat_Group2
            if is_common and c.name == 'Group' and '__root__' in '\n'.join(c.lines):
                continue

            # Skip Union wrapper classes with __root__ - we use Union aliases instead
            if '__root__' in '\n'.join(c.lines):
                # Skip Parameter wrapper in common.py
                if is_common and c.name == 'Parameter':
                    continue
                # Skip Group wrapper in common.py
                if is_common and c.name == 'Group':
                    continue
                # Skip Merge, Import, InsertControls wrappers in profile.py
                if fstem == 'profile' and c.name in ['Merge', 'Import', 'InsertControls']:
                    continue

            # Fix: Replace Group pipe unions
            lines_to_write = c.lines
            if fstem in ['common', 'catalog', 'profile']:
                prefix = 'Cat_' if fstem == 'catalog' else ('Prof_' if fstem == 'profile' else '')
                lines_to_write = []
                for line in c.lines:
                    # Replace Cat_Group2/Prof_Group2 with Group2
                    if prefix:
                        line = line.replace(f'{prefix}Group2', 'Group2')
                    # The Group Union wrapper (__root__: Group1|Group2) is removed by kill_roots()
                    # So we need to expand list[Group] references to list[Group1|Group2]
                    # This matches the OSCAL 1.2.0 schema where the groups field recursively
                    # references the Group anyOf definition (which can be either Group1 or Group2)
                    # Group1 is now in catalog.py and profile.py (not common.py)
                    if c.name == 'Group1' and 'list[Group]' in line:
                        line = line.replace('list[Group]', 'list[Group1|Group2]')
                    # Fix malformed Parameter pipe unions in profile.py
                    if fstem == 'profile':
                        line = line.replace('Parameter1|Parameter2Constraint', 'common.ParameterConstraint')
                        line = line.replace('Parameter1|Parameter2Guideline', 'common.ParameterGuideline')
                        line = line.replace('Parameter1|Parameter2Selection', 'common.ParameterSelection')
                    # Replace inline Parameter1|Parameter2 unions with common.Parameter alias
                    if 'Parameter1|Parameter2' in line:
                        line = line.replace('Parameter1|Parameter2', 'common.Parameter')
                    lines_to_write.append(line)

            out_file.writelines('\n'.join(lines_to_write) + '\n')

            # Add smart Union validators for Group1 and Group2 in catalog/profile
            if c.name == 'Group1' and fstem in ['catalog', 'profile']:
                out_file.write(add_union_validators('Group1', 'groups', 'Group2'))
            if c.name == 'Group2' and fstem in ['catalog', 'profile']:
                out_file.write(add_union_validators('Group2', 'controls', 'Group1'))

            # Add smart Union validators for Parameter1 and Parameter2 in common
            if c.name == 'Parameter1' and is_common:
                out_file.write(add_union_validators('Parameter1', 'values', 'Parameter2'))
            if c.name == 'Parameter2' and is_common:
                out_file.write(add_union_validators('Parameter2', 'select', 'Parameter1'))

            # add special validator for OscalVersion
            if c.name == 'OscalVersion':
                out_file.write(oscal_validator_code)
            # add alias HowMany
            if c.name == 'HowManyValidValues':
                out_file.write('HowMany = HowManyValidValues\n\n\n')
            # Track Parameter1 and Parameter2
            if c.name == 'Parameter1':
                seen_parameter1 = True
                # Add alias Parameter after both Parameter1 and Parameter2 have been emitted
                if seen_parameter2:
                    out_file.write('# Backward compatibility alias for OSCAL 1.2.0\n')
                    out_file.write('# Parameter split into Parameter1 (with values) and Parameter2 (with select)\n')
                    out_file.write('Parameter = Union[Parameter1, Parameter2]\n\n\n')
            if c.name == 'Parameter2':
                seen_parameter2 = True
                # Add alias Parameter after both Parameter1 and Parameter2 have been emitted
                if seen_parameter1:
                    out_file.write('# Backward compatibility alias for OSCAL 1.2.0\n')
                    out_file.write('# Parameter split into Parameter1 (with values) and Parameter2 (with select)\n')
                    out_file.write('Parameter = Union[Parameter1, Parameter2]\n\n\n')
            # Track Parties and Parties1
            if c.name == 'Parties':
                seen_parties = True
                # Add alias Party after both Parties and Parties1 have been emitted
                if seen_parties1:
                    out_file.write('# Backward compatibility alias for OSCAL 1.2.0\n')
                    out_file.write('# Party renamed to Parties (with variants Parties1 and Parties)\n')
                    out_file.write('Party = Union[Parties1, Parties]\n\n\n')
            if c.name == 'Parties1':
                seen_parties1 = True
                # Add alias Party after both Parties and Parties1 have been emitted
                if seen_parties:
                    out_file.write('# Backward compatibility alias for OSCAL 1.2.0\n')
                    out_file.write('# Party renamed to Parties (with variants Parties1 and Parties)\n')
                    out_file.write('Party = Union[Parties1, Parties]\n\n\n')
            # Track profile.py Union variants and add aliases after all variants are defined
            if fstem == 'profile':
                # Track SetParameters variants and update forward refs
                # TODO: Investigate why reordering doesn't place these classes in the right order
                if c.name == 'SetParameters':
                    seen_setparameters = True
                if c.name == 'SetParameters1':
                    # Update SetParameters forward references after both variants are defined
                    # They reference Parameter1|Parameter2Constraint and Parameter1|Parameter2Guideline
                    if seen_setparameters:
                        out_file.write(
                            '# Update SetParameters forward references for Parameter constraints/guidelines\n'
                        )
                        out_file.write('SetParameters.update_forward_refs()\n')
                        out_file.write('SetParameters1.update_forward_refs()\n\n\n')

                # Track Merge variants and add Union alias after all are defined
                if c.name == 'Merge1':
                    seen_merge1 = True
                    if seen_merge2 and seen_merge3:
                        out_file.write('# Union alias for Merge variants\n')
                        out_file.write('Merge = Union[Merge1, Merge2, Merge3]\n\n\n')
                if c.name == 'Merge2':
                    seen_merge2 = True
                    if seen_merge1 and seen_merge3:
                        out_file.write('# Union alias for Merge variants\n')
                        out_file.write('Merge = Union[Merge1, Merge2, Merge3]\n\n\n')
                if c.name == 'Merge3':
                    seen_merge3 = True
                    if seen_merge1 and seen_merge2:
                        out_file.write('# Union alias for Merge variants\n')
                        out_file.write('Merge = Union[Merge1, Merge2, Merge3]\n\n\n')

                # Track Import variants
                if c.name == 'Import1':
                    seen_import1 = True
                    # Add Import Union alias after both variants are defined
                    if seen_import2:
                        out_file.write('# Union alias for Import variants\n')
                        out_file.write('Import = Union[Import1, Import2]\n\n\n')
                if c.name == 'Import2':
                    seen_import2 = True
                    # Add Import Union alias after both variants are defined
                    if seen_import1:
                        out_file.write('# Union alias for Import variants\n')
                        out_file.write('Import = Union[Import1, Import2]\n\n\n')

                # Track InsertControls variants
                if c.name == 'InsertControls1':
                    seen_insertcontrols1 = True
                    # Add InsertControls Union alias after both variants are defined
                    if seen_insertcontrols2:
                        out_file.write('# Union alias for InsertControls variants\n')
                        out_file.write('InsertControls = Union[InsertControls1, InsertControls2]\n\n\n')
                if c.name == 'InsertControls2':
                    seen_insertcontrols2 = True
                    # Add InsertControls Union alias after both variants are defined
                    if seen_insertcontrols1:
                        out_file.write('# Union alias for InsertControls variants\n')
                        out_file.write('InsertControls = Union[InsertControls1, InsertControls2]\n\n\n')
            # Add backward compatibility alias for ControlSelections after ControlSelectionsAll
            if c.name == 'ControlSelectionsAll':
                out_file.write('# Backward compatibility alias\n')
                out_file.write('# DEPRECATED: Use ControlSelectionsAll instead\n')
                out_file.write('ControlSelection = ControlSelectionsAll\n\n\n')
            # Add backward compatibility alias for ControlObjectiveSelections after ControlObjectiveSelectionsAll
            if c.name == 'ControlObjectiveSelectionsAll':
                out_file.write('# Backward compatibility alias\n')
                out_file.write('# DEPRECATED: Use ControlObjectiveSelectionsAll instead\n')
                out_file.write('ControlObjectiveSelection = ControlObjectiveSelectionsAll\n\n\n')

        if not is_common:
            # Add backward compatibility alias for SetParameter in non-common modules
            # In OSCAL 1.2.0, SetParameter is only in common module
            if fstem in ['component', 'profile', 'ssp']:
                out_file.write('# Backward compatibility alias for OSCAL 1.2.0\n')
                out_file.write('# SetParameter is now only in common module\n')
                out_file.write('SetParameter = common.SetParameter\n\n\n')

            out_file.writelines('class Model(OscalBaseModel):\n')
            alias = alias_map[fstem]
            snake = alias.replace('-', '_')
            class_name = camel_map[fstem]
            if '-' in alias:
                out_file.writelines(f"    {snake}: {class_name} = Field(..., alias='{alias}')\n")
            else:
                out_file.writelines(f'    {snake}: {class_name}\n')

        if forward_refs:
            if not is_common:
                out_file.writelines('\n\n')
            # Filter out Group.update_forward_refs() from common.py since we removed the Group Union wrapper
            filtered_refs = forward_refs
            if is_common:
                filtered_refs = [ref for ref in forward_refs if not ref.startswith('Group.')]
            out_file.writelines('\n'.join(filtered_refs) + '\n')
        
        # Add backward compatibility aliases for Timing classes in common module
        if is_common:
            out_file.write('\n\n# Backward compatibility aliases for Timing classes\n')
            out_file.write('# Use TimingOnDate, TimingWithinDateRange, TimingAtFrequency instead\n')
            out_file.write('Timing = TimingOnDate\n')
            out_file.write('Timing1 = TimingWithinDateRange\n')
            out_file.write('Timing2 = TimingAtFrequency\n')


